import Brick from "./brick";

export function buildLevel(game, level) {
  let wall = [];
  level.forEach((row, rowIndex) => {
    row.forEach((brick, brickIndex) => {
      if (brick === 1) {
        let position = {
          x: 80 * brickIndex,
          y: 75 + 24 * rowIndex
        };
        wall.push(new Brick(game, position));
      }
    });
  });
  return wall;
}

